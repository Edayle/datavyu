  public void setDatabase(Database database)
  {
    this.db = database;

    DefaultListModel vocablm = (DefaultListModel)this.vocabList.getModel();
    DefaultListModel valueslm = (DefaultListModel)this.valuesList.getModel();

    // Remove old data
    vocablm.removeAllElements();
    valueslm.removeAllElements();

    // Nothing else to do if database is null
    if (database == null) {
      return;
    }

    try {
      Vector<MatrixVocabElement> mve = database.getMatrixVEs();

    } catch (SystemErrorException se) {
      JOptionPane.showMessageDialog(this, "An error occurred getting Matrix Vocab Elements: " + se);
      return;
    }
  }

